% plant tag for each row, and then record the first tag that was 
% extra as an easy stand count.  We found, over time, that it is 
% better to do stand counts on baby plants, and then save any extra tags 
% (due to plant deaths or counting errors) by bundling them together and
% stapling the bundles, inside-out, around the plant.  This way the
% tags are available if there is a tagging error without producing
% additional visual confusion and reducing waste.
%
% Kazic, 17.4.2018    

    
